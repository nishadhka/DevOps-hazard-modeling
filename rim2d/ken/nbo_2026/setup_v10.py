"""
Setup script for v10 — Expanded domain culvert inflow simulation.

v10 changes:
  - Expanded domain: LAT 19.49-19.57, LON 33.25-33.36 (~385x295 cells)
  - 38-day simulation (Jul 25 – Aug 31, 2025)
  - Full-domain sewershed
  - Outputs to v10/ subdirectory

Downloads GEE data (Copernicus DEM, ESA WorldCover, GHSL, MERIT Hydro),
computes HND, burns river channel using satellite water mask, creates
roughness / buildings / sewershed, and writes simulation_v10.def.

Usage:
    micromamba run -n zarrv3 python setup_v10.py
"""

