WORK_DIR = Path("/data/rim2d/nile_highres")
V10_DIR = WORK_DIR / "v10"
INPUT_DIR = V10_DIR / "input"
OUTPUT_DIR = V10_DIR / "output"
TIF_DIR = V10_DIR / "tif"

# Simulation parameters
SIM_DUR = 3283200      # 38 days in seconds (Jul 25 – Aug 31)
TSTEP_IN = 1800        # 30-min boundary time step
N_RAIN = 1824          # 38 days * 48 half-hours
PLUVIAL_DT = 1800      # 30-min rainfall time step

# Native HND parameters
DRAIN_ACC_THRESH = 1000  # min upstream cells to classify as drainage

# Stream-burn parameters (satellite channel mask from ESA WorldCover)
BURN_DEPTH = 3.0       # meters — depth to lower DEM at water pixels
NORMAL_DEPTH = 3.0     # meters — IWD at channel cells

# Manning's n remap (ESA WorldCover class -> roughness)
MANNING_FROM = [10, 20, 30, 40, 50, 60, 70, 80, 90, 95, 100]
MANNING_TO = [0.050, 0.040, 0.035, 0.035, 0.025, 0.030, 0.020, 0.025,
              0.040, 0.040, 0.030]

# MERIT Hydro native scale
MERIT_SCALE = 90  # meters
# GHSL native scale
GHSL_SCALE = 100  # meters


# ============================================================
# HELPER FUNCTIONS
