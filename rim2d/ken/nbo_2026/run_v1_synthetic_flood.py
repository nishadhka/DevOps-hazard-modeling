#!/usr/bin/env python3
"""
v1 Synthetic Hydrograph from Basin-Derived Catchment Rainfall — Nairobi.

Improvement over run_v1_river_inflow.py:
  - Catchment areas from HydroATLAS (delineate_watershed_v1.py) instead of
    flow-accumulation pixel counts
  - IMERG rainfall downloaded over the FULL BASIN extent (not just simulation
    domain) — captures upstream Nairobi/Athi highlands rainfall
  - Rational method + triangular UH with empirical tc from basin area
  - Runoff coefficient appropriate for tropical urban catchments

Also fixes the channel mask: replaces sparse ESA WorldCover class-80 mask
with a flow-accumulation-based river network (flwacc >= ACC_CHANNEL_THRESH).

Usage:
    cd /data/rim2d/nbo_2026
    # Step 1: delineate watersheds first (if not done)
    micromamba run -n zarrv3 python delineate_watershed_v1.py

    # Step 2: this script
    micromamba run -n zarrv3 python run_v1_synthetic_flood.py
"""

