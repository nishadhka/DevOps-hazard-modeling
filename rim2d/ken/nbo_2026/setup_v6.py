def step5_visualize(dem, burned_dem, iwd, combined_depth,
                    tdx_footprint, gap_cells, x, y):
    print()
    print("=" * 60)
    print("Step 5 — Visualization")
    print("=" * 60)
    VIS_DIR.mkdir(parents=True, exist_ok=True)

    from matplotlib.colors import LightSource
    ls = LightSource(azdeg=315, altdeg=45)
    hs = ls.hillshade(np.nan_to_num(dem, nan=0), vert_exag=3, dx=30, dy=30)

    x_km = (x - x[0]) / 1000.0
    y_km = (y - y[0]) / 1000.0
    ext  = [float(x_km[0]), float(x_km[-1]),
            float(y_km[0]), float(y_km[-1])]

    # Load v5 IWD for comparison
    v5_iwd, _, _ = load_nc(WORK_DIR / "v5" / "input" / "iwd.nc")
    v5_iwd = np.where(v5_iwd < 0, 0, v5_iwd)

    fig, axes = plt.subplots(2, 3, figsize=(22, 14))

    cmap_iwd = plt.cm.Blues
    vmax_iwd = 3.0

    # --- Panel 1: Layer A (TDX) ---
    ax = axes[0, 0]
    ax.imshow(hs, origin="lower", extent=ext, cmap="gray",
              vmin=0.3, vmax=1.0, alpha=0.55)
    layer_a_disp = np.ma.masked_where(~tdx_footprint, combined_depth)
    ax.imshow(layer_a_disp, origin="lower", extent=ext,
              cmap=cmap_iwd, vmin=0, vmax=vmax_iwd,
              alpha=0.85, interpolation="nearest")
    ax.set_title(f"Layer A — TDX-Hydro geometry\n"
                 f"({tdx_footprint.sum():,} cells, D=0.5–2.5m by order)",
                 fontsize=10, fontweight="bold")
    ax.set_xlabel("x (km)"); ax.set_ylabel("y (km)")

    # --- Panel 2: Layer B (HND gap-fill) ---
    ax = axes[0, 1]
    ax.imshow(hs, origin="lower", extent=ext, cmap="gray",
              vmin=0.3, vmax=1.0, alpha=0.55)
    layer_b_disp = np.ma.masked_where(~gap_cells,
                                       np.full_like(dem, HND_BURN_DEPTH))
    ax.imshow(layer_b_disp, origin="lower", extent=ext,
              cmap=plt.cm.Oranges, vmin=0, vmax=1.0,
              alpha=0.80, interpolation="nearest")
    ax.set_title(f"Layer B — MERIT HND gap-fill\n"
                 f"({gap_cells.sum():,} cells, D={HND_BURN_DEPTH}m uniform)",
                 fontsize=10, fontweight="bold")
    ax.set_xlabel("x (km)"); ax.set_ylabel("y (km)")

    # --- Panel 3: Combined v6 IWD ---
    ax = axes[0, 2]
    ax.imshow(hs, origin="lower", extent=ext, cmap="gray",
              vmin=0.3, vmax=1.0, alpha=0.55)
    iwd_disp = np.ma.masked_where(iwd <= 0, iwd)
    im3 = ax.imshow(iwd_disp, origin="lower", extent=ext,
                    cmap=cmap_iwd, vmin=0, vmax=vmax_iwd,
                    alpha=0.88, interpolation="nearest")
    fig.colorbar(im3, ax=ax, shrink=0.8, label="IWD (m)")
    wet = iwd > 0
    ax.set_title(f"v6 Combined IWD\n"
                 f"({wet.sum():,} cells, mean={iwd[wet].mean():.2f}m, "
                 f"max={iwd[wet].max():.2f}m)",
                 fontsize=10, fontweight="bold")
    ax.set_xlabel("x (km)"); ax.set_ylabel("y (km)")

    # --- Panel 4: Layer contribution map ---
    ax = axes[1, 0]
    ax.imshow(hs, origin="lower", extent=ext, cmap="gray",
              vmin=0.3, vmax=1.0, alpha=0.55)
    contrib = np.zeros((*dem.shape, 4), dtype=np.float32)
    contrib[tdx_footprint] = [0.10, 0.52, 0.90, 0.80]   # blue = TDX
    contrib[gap_cells]     = [0.90, 0.45, 0.10, 0.70]   # orange = HND gap
    ax.imshow(contrib, origin="lower", extent=ext, zorder=5)
    handles = [
        mpatches.Patch(color="#1a85e5", label=f"TDX geometry ({tdx_footprint.sum():,})"),
        mpatches.Patch(color="#e57320", label=f"HND gap-fill ({gap_cells.sum():,})"),
    ]
    ax.legend(handles=handles, loc="lower right", fontsize=9, framealpha=0.9)
    ax.set_title("Layer contribution\nBlue=TDX  Orange=HND gap-fill",
                 fontsize=10, fontweight="bold")
    ax.set_xlabel("x (km)"); ax.set_ylabel("y (km)")

    # --- Panel 5: v5 vs v6 difference ---
    ax = axes[1, 1]
    ax.imshow(hs, origin="lower", extent=ext, cmap="gray",
              vmin=0.3, vmax=1.0, alpha=0.45)
    diff = iwd - v5_iwd
    diff_disp = np.ma.masked_where((iwd == 0) & (v5_iwd == 0), diff)
    im5 = ax.imshow(diff_disp, origin="lower", extent=ext,
                    cmap=plt.cm.RdBu_r, vmin=-2.0, vmax=2.0,
                    alpha=0.85, interpolation="nearest")
    fig.colorbar(im5, ax=ax, shrink=0.8, label="Δ IWD (m)")
    new_cells = int(((iwd > 0) & (v5_iwd == 0)).sum())
    ax.set_title(f"v6 − v5 difference\n"
                 f"New cells from HND: {new_cells:,}",
                 fontsize=10, fontweight="bold")
    ax.set_xlabel("x (km)"); ax.set_ylabel("y (km)")

    # --- Panel 6: Depth histogram comparison ---
    ax = axes[1, 2]
    for label, arr, color in [
        ("v5 TDX only",    v5_iwd, "#0984e3"),
        ("v6 combined",    iwd,    "#00b894"),
    ]:
        vals = arr[arr > 0].flatten()
        ax.hist(vals, bins=40, alpha=0.6, color=color, density=True,
                label=f"{label}  (n={len(vals):,}, μ={vals.mean():.2f}m)")
    ax.axvline(HND_BURN_DEPTH, color="#e17055", ls="--", lw=1.2,
               label=f"HND burn depth ({HND_BURN_DEPTH}m)")
    ax.set_xlabel("IWD (m)")
    ax.set_ylabel("Density")
    ax.set_title("Depth distribution: v5 vs v6",
                 fontsize=10, fontweight="bold")
    ax.legend(fontsize=9, framealpha=0.9)

    for ax in axes.flat:
        if hasattr(ax, 'xaxis'):
            ax.xaxis.set_major_formatter(plt.FuncFormatter(lambda v, _: f"{v:.0f}"))
            ax.yaxis.set_major_formatter(plt.FuncFormatter(lambda v, _: f"{v:.0f}"))

    fig.suptitle(
        "Nairobi v6 — Combined TDX-Hydro + MERIT HND IWD\n"
        f"Layer A: TDX geometry (orders 2–5)  ·  "
        f"Layer B: HND gap-fill ({HND_BURN_DEPTH}m headwaters)",
        fontsize=13, fontweight="bold"
    )
    fig.tight_layout()
    out = VIS_DIR / "v6_combined_iwd.png"
    fig.savefig(str(out), dpi=DPI, bbox_inches="tight", facecolor="white")
    plt.close(fig)
    print(f"  Saved: {out}")


