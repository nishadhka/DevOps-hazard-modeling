def tif_to_rim2d_arrays(tif_path):
    with rasterio.open(str(tif_path)) as src:
        data      = src.read(1).astype(np.float64)
        transform = src.transform
        nodata    = src.nodata
        crs       = src.crs
    nrows, ncols = data.shape
    x     = transform.c + transform.a * (np.arange(ncols) + 0.5)
    y_top = transform.f + transform.e * (np.arange(nrows) + 0.5)
    y     = y_top[::-1]
    data  = data[::-1, :].copy()
    if nodata is not None:
        data[np.isclose(data, nodata)] = np.nan
    data[~np.isfinite(data)] = np.nan
    return data, x, y, transform, crs


def load_nc(path):
    ds = netCDF4.Dataset(str(path))
    x = np.array(ds["x"][:])
    y = np.array(ds["y"][:])
    var = [v for v in ds.variables if v not in ("x", "y")][0]
    data = np.array(ds[var][:], dtype=np.float64)
    ds.close()
    data[data < -9000] = np.nan
    return data, x, y


def write_rim2d_nc(data, x, y, nc_path, varname="Band1"):
    fill = -9999.0
    arr  = np.where(np.isnan(data), fill, data).astype(np.float32)
    ds = netCDF4.Dataset(str(nc_path), "w", format="NETCDF3_CLASSIC")
    ds.Conventions = "CF-1.5"
    ds.history     = "Generated by setup_v6.py (Nairobi v6)"
    ds.createDimension("x", arr.shape[1])
    ds.createDimension("y", arr.shape[0])
    xv = ds.createVariable("x", "f8", ("x",)); xv[:] = x
    xv.long_name = "x coordinate"; xv.standard_name = "projection_x_coordinate"; xv.units = "m"
    yv = ds.createVariable("y", "f8", ("y",)); yv[:] = y
    yv.long_name = "y coordinate"; yv.standard_name = "projection_y_coordinate"; yv.units = "m"
    bv = ds.createVariable(varname, "f4", ("y", "x"), fill_value=np.float32(fill))
    bv[:] = arr
    ds.close()


# ---------------------------------------------------------------------------
# Step 1 — Layer A: TDX-Hydro geometry burn
# ---------------------------------------------------------------------------

def step1_tdx_burn(dem, x, y, ref_crs):
    print("=" * 60)
    print("Step 1 — Layer A: TDX-Hydro geometry burn")
    print("=" * 60)

    gdf = gpd.read_file(str(TDX_RIVER_PATH)).to_crs(ref_crs)
    gdf["rivwth"] = gdf["stream_order"].map(lambda o: float(ORDER_WIDTH.get(int(o), 15)))
    gdf["rivdph"] = gdf["stream_order"].map(lambda o: float(ORDER_DEPTH.get(int(o), 0.5)))

    for order in sorted(gdf["stream_order"].unique()):
        n = (gdf["stream_order"] == order).sum()
        print(f"  Order {order}: {n:3d} segments  "
              f"W={ORDER_WIDTH[int(order)]}m  D={ORDER_DEPTH[int(order)]}m")

    nrows, ncols = dem.shape
    dx = x[1] - x[0]
    dy = y[1] - y[0]
    ras_transform = rasterio.transform.from_origin(
        x[0] - dx / 2,
        y[-1] + dy / 2,
        dx, dy
    )

    tdx_footprint = np.zeros((nrows, ncols), dtype=bool)
    depth_layer_a = np.zeros((nrows, ncols), dtype=np.float64)

    for _, row in gdf.iterrows():
        geom = row.geometry
        w    = float(row["rivwth"])
        d    = float(row["rivdph"])
        if geom.is_empty:
            continue
        buf  = geom.buffer(w / 2.0)
        mask = rio_rasterize(
            [(mapping(buf), 1)],
            out_shape=(nrows, ncols),
            transform=ras_transform,
            fill=0, dtype=np.uint8,
        )[::-1, :]   # flip to y-ascending
        cells = mask > 0
        tdx_footprint[cells] = True
        depth_layer_a[cells] = np.maximum(depth_layer_a[cells], d)

    n_tdx = int(tdx_footprint.sum())
    print(f"\n  TDX footprint cells : {n_tdx:,}  "
          f"({100 * n_tdx / dem.size:.2f}%)")
    print(f"  Depth range (A)     : {depth_layer_a[tdx_footprint].min():.2f} – "
          f"{depth_layer_a[tdx_footprint].max():.2f} m")

    return tdx_footprint, depth_layer_a


# ---------------------------------------------------------------------------
# Step 2 — Layer B: MERIT HND gap-fill
# ---------------------------------------------------------------------------

def step2_hnd_fill(dem, x, y, tdx_footprint):
    print()
    print("=" * 60)
    print("Step 2 — Layer B: MERIT HND headwater gap-fill")
    print(f"  HND = 0 cells, outside TDX footprint → burn {HND_BURN_DEPTH}m")
    print("=" * 60)

    hnd, hnd_x, hnd_y = load_nc(HND_PATH)

    # HND is on same 30m grid (from v1 setup) — verify shape match
    if hnd.shape != dem.shape:
        raise RuntimeError(
            f"HND shape {hnd.shape} != DEM shape {dem.shape}. "
            "Re-run setup_v1.py to regenerate hnd_30m.nc on the correct grid."
        )

    # Drainage cells: HND == 0 (exactly on drainage line)
    hnd_drain = (hnd == 0.0)

    # Gap-fill: HND drainage cells NOT covered by TDX
    gap_cells = hnd_drain & ~tdx_footprint

    n_hnd   = int(hnd_drain.sum())
    n_gap   = int(gap_cells.sum())
    n_tdx   = int(tdx_footprint.sum())
    n_overlap = int((hnd_drain & tdx_footprint).sum())

    print(f"  HND=0 cells total   : {n_hnd:,}")
    print(f"  Already in TDX      : {n_overlap:,}")
    print(f"  Gap-fill (layer B)  : {n_gap:,}  "
          f"({100 * n_gap / dem.size:.2f}%)")

    depth_layer_b = np.where(gap_cells, HND_BURN_DEPTH, 0.0)
    return gap_cells, depth_layer_b


# ---------------------------------------------------------------------------
# Step 3 — Merge layers, burn DEM, compute IWD
# ---------------------------------------------------------------------------

def step3_merge_and_burn(dem, tdx_footprint, depth_layer_a,
                          gap_cells, depth_layer_b):
    print()
    print("=" * 60)
    print("Step 3 — Merge layers A + B, burn DEM")
    print("=" * 60)

    # Combined depth: A takes priority, B fills gaps
    combined_depth = np.zeros_like(dem)
    combined_depth[tdx_footprint] = depth_layer_a[tdx_footprint]
    combined_depth[gap_cells]     = depth_layer_b[gap_cells]

    channel_mask = (combined_depth > 0).astype(np.float64)
    burned_dem   = dem.copy()
    burned_dem[channel_mask > 0] -= combined_depth[channel_mask > 0]

    iwd = np.maximum(dem - burned_dem, 0.0)
    iwd[~np.isfinite(iwd)] = 0.0

    wet = iwd > 0
    n_a   = int(tdx_footprint.sum())
    n_b   = int(gap_cells.sum())
    print(f"  Layer A (TDX)       : {n_a:,} cells")
    print(f"  Layer B (HND gap)   : {n_b:,} cells")
    print(f"  Total wet cells     : {wet.sum():,}  "
          f"({100 * wet.sum() / dem.size:.2f}%)")
    print(f"  IWD range           : {iwd[wet].min():.2f} – {iwd[wet].max():.2f} m")
    print(f"  Burned DEM min      : {np.nanmin(burned_dem):.1f} m")

    return burned_dem, iwd, channel_mask, combined_depth


# ---------------------------------------------------------------------------
# Step 4 — Save outputs
# ---------------------------------------------------------------------------

def step4_save(burned_dem, iwd, channel_mask, x, y):
    print()
    print("=" * 60)
    print("Step 4 — Save v6 inputs")
    print("=" * 60)
    INPUT_DIR.mkdir(parents=True, exist_ok=True)

    write_rim2d_nc(burned_dem,   x, y, INPUT_DIR / "dem.nc")
    print("  Written: dem.nc")
    write_rim2d_nc(iwd,          x, y, INPUT_DIR / "iwd.nc")
    print("  Written: iwd.nc")
    write_rim2d_nc(channel_mask, x, y, INPUT_DIR / "channel_mask.nc")
    print("  Written: channel_mask.nc")

    roughness_src = V3B_INPUT / "roughness.nc"
    if not roughness_src.exists():
        roughness_src = V1_INPUT / "roughness.nc"
        print("  NOTE: v3b roughness not found, using v1")

    for fname, src in {
        "roughness.nc":         roughness_src,
        "buildings.nc":         V1_INPUT / "buildings.nc",
        "sealed_surface.nc":    V1_INPUT / "sealed_surface.nc",
        "pervious_surface.nc":  V1_INPUT / "pervious_surface.nc",
        "sewershed_v1_full.nc": V1_INPUT / "sewershed_v1_full.nc",
    }.items():
        dst = INPUT_DIR / fname
        if Path(src).exists():
            shutil.copy2(str(src), str(dst))
            print(f"  Copied:  {fname}")
        else:
            print(f"  WARNING: {fname} not found")


