# TDX width/depth by stream order
ORDER_WIDTH = {2: 15,  3: 30,  4: 60,  5: 120}   # metres
ORDER_DEPTH = {2: 0.5, 3: 1.0, 4: 1.5, 5:  2.5}  # metres

# HND headwater burn depth (layer B gap-fill)
HND_BURN_DEPTH = 0.3   # m

DPI = 150

