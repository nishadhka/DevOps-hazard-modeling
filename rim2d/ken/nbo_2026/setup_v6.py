def main():
    print("=" * 60)
    print("Nairobi v6 — Combined TDX-Hydro + MERIT HND IWD")
    print(f"  TDX widths  : {ORDER_WIDTH}")
    print(f"  TDX depths  : {ORDER_DEPTH}")
    print(f"  HND burn    : {HND_BURN_DEPTH} m (gap-fill only)")
    print("=" * 60)

    print("\nLoading DEM...")
    dem, x, y, ref_transform, ref_crs = tif_to_rim2d_arrays(V1_TIF / "dem.tif")
    print(f"  Shape: {dem.shape}  CRS: {ref_crs}")
    print(f"  Range: {np.nanmin(dem):.1f} – {np.nanmax(dem):.1f} m")

    # Layer A — TDX geometry burn
    tdx_footprint, depth_layer_a = step1_tdx_burn(dem, x, y, ref_crs)

    # Layer B — HND gap-fill
    gap_cells, depth_layer_b = step2_hnd_fill(dem, x, y, tdx_footprint)

    # Merge + burn
    burned_dem, iwd, channel_mask, combined_depth = step3_merge_and_burn(
        dem, tdx_footprint, depth_layer_a, gap_cells, depth_layer_b
    )

    # Save
    step4_save(burned_dem, iwd, channel_mask, x, y)

    # Visualize
    step5_visualize(dem, burned_dem, iwd, combined_depth,
                    tdx_footprint, gap_cells, x, y)

    print()
    print("=" * 60)
    print("v6 setup complete.")
    print(f"  Inputs : {INPUT_DIR}/")
    print(f"  Plots  : {VIS_DIR}/")
    print()
    print("Layer breakdown:")
    print(f"  A (TDX)      : {tdx_footprint.sum():,} cells  0.5–2.5m depth")
    print(f"  B (HND gaps) : {gap_cells.sum():,} cells  {HND_BURN_DEPTH}m depth")
    print(f"  Total        : {int(tdx_footprint.sum() + gap_cells.sum()):,} cells")
    print()
    print("Next: create simulation_v6ss.def and run steady-state")
    print("=" * 60)


if __name__ == "__main__":
    main()
