#!/usr/bin/env python3
"""
Nairobi v6 — Combined MERIT HND + TDX-Hydro IWD.

Merges the two complementary methods:

  Layer A — TDX-Hydro geometry burn (from v5)
            River segments orders 2–5, width/depth by stream order.
            Buffer each centerline by rivwth/2, lower DEM by rivdph.
            Orders 3–5 get full geometry; order 2 gets narrow/shallow burn.

  Layer B — MERIT HND drainage fill (from v1 approach)
            Cells where HND = 0 (sitting exactly on a drainage line)
            that are NOT already covered by a TDX channel footprint.
            Applied with a small headwater burn depth (HND_BURN_DEPTH).

Priority rule: TDX footprint always wins — HND fill only applied in gaps.

Result:
  - Physically correct width/depth on 141 mapped TDX channels
  - Continuous drainage connectivity via HND=0 headwater cells
  - No double-counting between layers

Parameters:
  ORDER_WIDTH  = {2:15, 3:30, 4:60, 5:120}  m
  ORDER_DEPTH  = {2:0.5, 3:1.0, 4:1.5, 5:2.5}  m
  HND_BURN_DEPTH = 0.3 m  (headwater channel seed depth)

Outputs → v6/input/
  dem.nc, iwd.nc, channel_mask.nc
  roughness.nc (from v3b), + buildings, sealed, pervious, sewershed

Usage:
    cd /data/rim2d/nbo_2026
    micromamba run -n hydromt-wflow python setup_v6.py
"""

