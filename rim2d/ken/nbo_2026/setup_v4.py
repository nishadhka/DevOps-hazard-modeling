#!/usr/bin/env python3
"""
Nairobi v4 — Steady-State Pre-Simulation IWD.

Generates realistic Initial Water Depth by running RIM2D with constant
low baseflow inflow at all CROSSING river entry points until the model
reaches steady state.  The final water depth output becomes iwd.nc for v4.

Method (from IWD_ATTEMPTS.md recommendation):
  The ideal approach is a pre-simulation steady-state run, as done for
  example_fluvial ('IWD162.5m.nc').  Nairobi terrain is steeply sloped
  (1440–2400 m), so gravity-driven steady state should converge in ~12 h.

Workflow:
  Step 1  Compute baseflow Q per CROSSING entry from catchment area
          using the FAO low-flow yield formula:
            Q_base = BASEFLOW_YIELD * UP_AREA (m³/s)
  Step 2  Convert Q_base → constant WSE using Manning's equation
            depth = (Q * n / (W * S^0.5))^(3/5),  WSE = bed_elev + depth
  Step 3  Write inflowlocs_v4ss.txt  (constant WSE, N+1 steps, 12 h run)
          Write fluvbound_mask_v4.nc (1-based index per entry cell)
          Write simulation_v4ss.def  (no rain, IWD=0, fluv_bound=TRUE)
  Step 4  Run RIM2D:
            cd v4 && ../../bin/RIM2D simulation_v4ss.def --def flex
  Step 5  Extract IWD from last water-depth output:
            python setup_v4.py --extract-iwd

Outputs in v4/:
  input/inflowlocs_v4ss.txt     steady-state inflow boundary conditions
  input/fluvbound_mask_v4.nc    fluvial boundary cell index mask
  simulation_v4ss.def           RIM2D definition file
  input/iwd.nc                  (after --extract-iwd) final water depth

Usage:
    cd /data/rim2d/nbo_2026
    # Step 1-3: generate inputs
    micromamba run -n zarrv3 python setup_v4.py
    # Step 4: run simulation
    cd v4 && export LD_LIBRARY_PATH=/data/rim2d/lib:$LD_LIBRARY_PATH
    ../../bin/RIM2D simulation_v4ss.def --def flex
    cd ..
    # Step 5: extract IWD
    micromamba run -n zarrv3 python setup_v4.py --extract-iwd
"""

import argparse
import shutil
from pathlib import Path

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import matplotlib.colors as mcolors
import netCDF4
import numpy as np
from pyproj import Transformer

# ---------------------------------------------------------------------------
# Paths
# ---------------------------------------------------------------------------
WORK_DIR   = Path(__file__).resolve().parent
V1_INPUT   = WORK_DIR / "v1" / "input"
V4_DIR     = WORK_DIR / "v4"
INPUT_DIR  = V4_DIR / "input"
OUTPUT_DIR = V4_DIR / "output"
VIS_DIR    = V4_DIR / "visualizations"

ENTRIES_CSV = V1_INPUT / "river_entries_v1.csv"
DEM_PATH    = V1_INPUT / "dem.nc"

# ---------------------------------------------------------------------------
# Steady-state simulation parameters
# ---------------------------------------------------------------------------
SS_DUR_S    = 43200    # 12 h — enough for steep Nairobi terrain to equilibrate
SS_DT_S     = 1800     # 30 min WSE update interval
SS_N_STEPS  = SS_DUR_S // SS_DT_S    # 24 steps → 25 WSE values per cell

# Manning's channel geometry (consistent with v2)
MANNINGS_N  = 0.035
CHANNEL_W_BY_ORDER = {5: 25.0, 4: 15.0, 3: 8.0, 2: 4.0}   # m, wider for higher orders
CHANNEL_S   = 0.005   # bed slope (m/m) — Nairobi average

# Baseflow yield (m³/s per km²) — represents dry-season low flow
# FAO/GRDC global median for East Africa: ~0.5–2 L/s/km²
BASEFLOW_YIELD = 0.001   # 1 L/s/km² = 1e-3 m³/s/km²

# Catchment areas for CROSSING entries by stream order (km²)
# Same as v2 — anchored to HydroATLAS medians for Nairobi
UP_AREA_BY_ORDER = {5: 1500.0, 4: 375.0, 3: 95.0, 2: 24.0}

TO_UTM = Transformer.from_crs("EPSG:4326", "EPSG:32737", always_xy=True)

DPI = 150


# ---------------------------------------------------------------------------
# I/O helpers
# ---------------------------------------------------------------------------

def load_nc(path):
    ds = netCDF4.Dataset(str(path))
    x  = np.array(ds["x"][:])
    y  = np.array(ds["y"][:])
    vn = [v for v in ds.variables if v not in ("x", "y")][0]
    data = np.array(ds[vn][:], dtype=np.float64)
    ds.close()
    data[data < -9000] = np.nan
    return data, x, y


def write_nc(data, x, y, path, varname="Band1"):
    fill = -9999.0
    arr  = np.where(np.isnan(data), fill, data).astype(np.float32)
    nrows, ncols = arr.shape
    ds = netCDF4.Dataset(str(path), "w", format="NETCDF3_CLASSIC")
    ds.Conventions = "CF-1.5"
    ds.history     = "Generated by setup_v4.py (Nairobi v4 steady-state IWD)"
    ds.createDimension("x", ncols)
    ds.createDimension("y", nrows)
    xv = ds.createVariable("x", "f8", ("x",))
    xv[:] = x
    xv.long_name = "x coordinate"
    xv.standard_name = "projection_x_coordinate"
    xv.units = "m"
    yv = ds.createVariable("y", "f8", ("y",))
    yv[:] = y
    yv.long_name = "y coordinate"
    yv.standard_name = "projection_y_coordinate"
    yv.units = "m"
    bv = ds.createVariable(varname, "f4", ("y", "x"),
                           fill_value=np.float32(fill))
    bv[:] = arr
    ds.close()


def grid_index(lon, lat, x_arr, y_arr):
    ex, ey = TO_UTM.transform(lon, lat)
    ix = int(np.argmin(np.abs(x_arr - ex)))
    iy = int(np.argmin(np.abs(y_arr - ey)))
    return (np.clip(ix, 0, len(x_arr) - 1),
            np.clip(iy, 0, len(y_arr) - 1))


def q_to_wse(q, bed_elev, stream_order):
    """Manning's wide rectangular channel → WSE."""
    w = CHANNEL_W_BY_ORDER.get(stream_order, 8.0)
    depth = (max(q, 1e-6) * MANNINGS_N / (w * CHANNEL_S**0.5)) ** (3.0 / 5.0)
    return bed_elev + depth


# ---------------------------------------------------------------------------
# Step 1+2: Baseflow Q and WSE for CROSSING entries
# ---------------------------------------------------------------------------

def compute_baseflow_entries(x_arr, y_arr, dem):
    """Load CROSSING entries and compute constant baseflow WSE."""
    import csv

    entries = []
    with open(str(ENTRIES_CSV)) as f:
        for row in csv.DictReader(f):
            if row["category"] == "crossing":
                entries.append({
                    "entry_id":    int(row["entry_id"]),
                    "stream_order": int(row["stream_order"]),
                    "lon":         float(row["lon"]),
                    "lat":         float(row["lat"]),
                    "linkno":      int(row["linkno"]),
                })

    print(f"  CROSSING entries: {len(entries)}")
    print(f"  {'ID':>4}  {'Order':>5}  {'Area km²':>9}  "
          f"{'Q_base m³/s':>12}  {'Depth m':>8}  {'WSE m':>10}")
    print("  " + "-" * 56)

    for e in entries:
        so    = e["stream_order"]
        area  = UP_AREA_BY_ORDER.get(so, 24.0)
        q     = BASEFLOW_YIELD * area          # m³/s
        ix, iy = grid_index(e["lon"], e["lat"], x_arr, y_arr)
        bed   = float(dem[iy, ix]) if np.isfinite(dem[iy, ix]) else 1600.0
        wse   = q_to_wse(q, bed, so)
        depth = wse - bed
        e.update({"area_km2": area, "q_base": q, "bed_elev": bed,
                  "wse": wse, "depth": depth, "ix": ix, "iy": iy})
        print(f"  {e['entry_id']:>4}  {so:>5}  {area:>9.0f}  "
              f"{q:>12.3f}  {depth:>8.3f}  {wse:>10.3f}")

    return entries


# ---------------------------------------------------------------------------
# Step 3a: Write inflowlocs
# ---------------------------------------------------------------------------

def write_inflowlocs(entries):
    """
    RIM2D inflowlocs format (flex, confirmed from readin_flex.f90):
      Line 1: sim_dur_s
      Line 2: dt_s
      Line 3: n_cells
      Each cell: iy1  ix1  wse_0  wse_1  ...  wse_N   (N+1 values)
    WSE is constant throughout — this is a steady-state forcing.
    """
    path = INPUT_DIR / "inflowlocs_v4ss.txt"
    n = len(entries)
    lines = [str(SS_DUR_S), str(SS_DT_S), str(n)]

    for e in entries:
        iy1 = e["iy"] + 1   # 1-based
        ix1 = e["ix"] + 1
        # N+1 values: prepend bed_elev at t=0 (dry start), then constant WSE
        wse_vals = [e["bed_elev"]] + [e["wse"]] * SS_N_STEPS
        wse_str  = "\t".join(f"{w:.4f}" for w in wse_vals)
        lines.append(f"{iy1}\t{ix1}\t{wse_str}")

    with open(str(path), "w") as f:
        f.write("\n".join(lines) + "\n")
    print(f"  Written: {path.name}  "
          f"({n} entries × {SS_N_STEPS} steps + t=0)")
    return path


# ---------------------------------------------------------------------------
# Step 3b: Write fluvbound_mask
# ---------------------------------------------------------------------------

def write_fluvbound_mask(entries, x_arr, y_arr, dem):
    """1-based index per entry cell; RIM2D uses maxval(mask) = n_entries."""
    mask = np.zeros_like(dem, dtype=np.float32)
    for i, e in enumerate(entries):
        mask[e["iy"], e["ix"]] = float(i + 1)
    path = INPUT_DIR / "fluvbound_mask_v4.nc"
    write_nc(mask, x_arr, y_arr, path)
    print(f"  Written: fluvbound_mask_v4.nc  "
          f"({int((mask > 0).sum())} cells, max={int(mask.max())})")


# ---------------------------------------------------------------------------
