def write_dummy_outflowlocs():
    """RIM2D requires out_cells file; write a minimal 1-cell placeholder."""
    path = INPUT_DIR / "outflowlocs_v4.txt"
    # Format: noutloc / outtstep / iy ix name (1-based)
    path.write_text("1\n1800\n1 1 dummy\n")
    print(f"  Written: {path.name}  (placeholder)")


# ---------------------------------------------------------------------------
# Step 5: Extract IWD from last steady-state output
# ---------------------------------------------------------------------------

def extract_iwd():
    import glob, os, re

    VIS_DIR.mkdir(parents=True, exist_ok=True)

    all_wd = glob.glob(str(OUTPUT_DIR / "nbo_v4ss_wd_*.nc"))
    wd_files = sorted(
        [f for f in all_wd
         if re.search(r"nbo_v4ss_wd_\d+\.nc$", os.path.basename(f))],
        key=lambda p: int(re.search(r"nbo_v4ss_wd_(\d+)\.nc",
                                     os.path.basename(p)).group(1))
    )
    if not wd_files:
        print("ERROR: No water depth output files found in v4/output/")
        print("       Run the simulation first.")
        return

    last_file = wd_files[-1]
    t_sec = int(re.search(r"nbo_v4ss_wd_(\d+)\.nc",
                           os.path.basename(last_file)).group(1))
    print(f"  Using last output: {os.path.basename(last_file)}  "
          f"(t = {t_sec}s = {t_sec/3600:.1f}h)")

    wd, x_arr, y_arr = load_nc(last_file)

    # Cap unrealistic boundary artefacts
    wd = np.where(wd > 20, 0.0, wd)
    wd = np.where(wd < 0,  0.0, wd)

    wet = wd > 0.01
    print(f"  Wet cells  : {int(np.sum(wet)):,}  "
          f"({100 * np.sum(wet) / wd.size:.2f}%)")
    print(f"  Depth range: {float(wd[wet].min()):.3f} – "
          f"{float(wd[wet].max()):.3f} m")
    print(f"  Mean depth : {float(wd[wet].mean()):.3f} m")

    iwd_path = INPUT_DIR / "iwd.nc"
    write_nc(wd, x_arr, y_arr, iwd_path)
    print(f"  Written: {iwd_path}")

    # Copy all other rasters from v1 (if not already done)
    copy_list = [
        "channel_mask.nc", "roughness.nc", "buildings.nc",
        "sealed_surface.nc", "pervious_surface.nc", "sewershed_v1_full.nc",
    ]
    for fname in copy_list:
        src = V1_INPUT / fname
        dst = INPUT_DIR / fname
        if src.exists() and not dst.exists():
            shutil.copy2(str(src), str(dst))
            print(f"  Copied:  {fname}  (from v1/input)")

    # Visualize IWD
    visualize_iwd(wd, x_arr, y_arr, t_sec)

    print("\nv4 IWD extraction complete.")
    print("  Next: create event simulation def pointing to v4/input/iwd.nc")


def visualize_iwd(wd, x_arr, y_arr, t_sec):
    """4-panel verification of the extracted steady-state IWD."""
    dem, _, _ = load_nc(DEM_PATH)
    buildings, _, _ = load_nc(V1_INPUT / "buildings.nc")
    buildings = np.flipud(buildings)

    from matplotlib.colors import LightSource
    ls = LightSource(azdeg=315, altdeg=45)
    hs = ls.hillshade(np.nan_to_num(dem, nan=0), vert_exag=3, dx=30, dy=30)

    ext_m = [float(x_arr[0]), float(x_arr[-1]),
             float(y_arr[0]), float(y_arr[-1])]

    fig, axes = plt.subplots(1, 3, figsize=(22, 8))

    # --- Panel 1: IWD depth map ---
    ax = axes[0]
    ax.imshow(hs, origin="lower", extent=ext_m, cmap="gray",
              vmin=0.3, vmax=1.0, alpha=0.6)
    wd_m = np.ma.masked_where(wd < 0.01, wd)
    im = ax.imshow(wd_m, origin="lower", extent=ext_m,
                   cmap="Blues", vmin=0, vmax=3.0,
                   interpolation="nearest", alpha=0.85)
    fig.colorbar(im, ax=ax, shrink=0.75, label="Water depth (m)")
    wet = wd > 0.01
    ax.set_title(
        f"v4 Steady-State IWD  (t={t_sec/3600:.0f}h)\n"
        f"Wet cells: {int(np.sum(wet)):,} ({100*np.sum(wet)/wd.size:.2f}%)",
        fontsize=11, fontweight="bold"
    )
    ax.set_xlabel("Easting (m UTM 37S)")
    ax.set_ylabel("Northing (m UTM 37S)")

    # --- Panel 2: depth histogram ---
    ax = axes[1]
    vals = wd[wet]
    ax.hist(vals, bins=50, color="#1a66ff", alpha=0.8, edgecolor="white")
    ax.axvline(float(np.median(vals)), color="red", lw=1.5,
               label=f"Median: {float(np.median(vals)):.2f} m")
    ax.axvline(float(np.mean(vals)), color="orange", lw=1.5, linestyle="--",
               label=f"Mean: {float(np.mean(vals)):.2f} m")
    ax.set_xlabel("Water depth (m)")
    ax.set_ylabel("Cell count")
    ax.set_title("IWD depth distribution\n(wet cells only)",
                 fontsize=11, fontweight="bold")
    ax.legend(fontsize=9)
    ax.grid(alpha=0.3)

    # --- Panel 3: IWD + buildings overlay ---
    ax = axes[2]
    ax.imshow(hs, origin="lower", extent=ext_m, cmap="gray",
              vmin=0.3, vmax=1.0, alpha=0.6)
    ax.imshow(wd_m, origin="lower", extent=ext_m,
              cmap="Blues", vmin=0, vmax=3.0,
              interpolation="nearest", alpha=0.85)
    bldg = buildings > 0
    if np.any(bldg):
        ax.contour(bldg.astype(float), levels=[0.5],
                   origin="lower", extent=ext_m,
                   colors=["#ff8800"], linewidths=0.5, alpha=0.6)
    ax.set_title("IWD + buildings (orange)\n"
                 "Steady-state river channels",
                 fontsize=11, fontweight="bold")
    ax.set_xlabel("Easting (m UTM 37S)")

    for ax in axes:
        ax.xaxis.set_major_formatter(
            plt.FuncFormatter(lambda v, _: f"{v/1000:.0f}"))
        ax.yaxis.set_major_formatter(
            plt.FuncFormatter(lambda v, _: f"{v/1000:.0f}"))

    fig.suptitle(
        "Nairobi v4 — Steady-State IWD from RIM2D Pre-Simulation\n"
        f"12h constant baseflow, 16 CROSSING entry points",
        fontsize=13, fontweight="bold"
    )
    fig.tight_layout()
    out = VIS_DIR / "v4_steadystate_iwd.png"
    fig.savefig(str(out), dpi=DPI, bbox_inches="tight", facecolor="white")
    plt.close(fig)
    print(f"  Saved: {out}")


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

wd_files_used = []   # populated in extract_iwd, used by visualize_iwd title

def main():
    global wd_files_used

    parser = argparse.ArgumentParser()
    parser.add_argument("--extract-iwd", action="store_true",
                        help="Extract IWD from last SS output (run after simulation)")
    args = parser.parse_args()

    if args.extract_iwd:
        print("=" * 60)
        print("Nairobi v4 — Extract IWD from steady-state output")
        print("=" * 60)
        INPUT_DIR.mkdir(parents=True, exist_ok=True)
        extract_iwd()
        return

    # ---- Generate inputs ----
    print("=" * 60)
    print("Nairobi v4 — Steady-State IWD Setup")
    print(f"  SS duration  : {SS_DUR_S}s ({SS_DUR_S/3600:.0f}h)")
    print(f"  SS time step : {SS_DT_S}s ({SS_DT_S/60:.0f} min)")
    print(f"  WSE steps/cell: {SS_N_STEPS + 1}  (incl. t=0 dry start)")
    print(f"  Baseflow yield: {BASEFLOW_YIELD*1000:.1f} L/s/km²")
    print("=" * 60)

    INPUT_DIR.mkdir(parents=True, exist_ok=True)
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    VIS_DIR.mkdir(parents=True, exist_ok=True)

    dem, x_arr, y_arr = load_nc(DEM_PATH)
    print(f"\nLoaded DEM: {dem.shape[1]}×{dem.shape[0]} cells  "
          f"({(x_arr[-1]-x_arr[0])/1000:.1f}×{(y_arr[-1]-y_arr[0])/1000:.1f} km)")

    print("\nStep 1+2 — Baseflow Q and WSE per CROSSING entry:")
    entries = compute_baseflow_entries(x_arr, y_arr, dem)
    wd_files_used = entries

    print("\nStep 3 — Writing simulation inputs:")
    write_inflowlocs(entries)
    write_fluvbound_mask(entries, x_arr, y_arr, dem)
    write_dummy_outflowlocs()
    write_def(len(entries))

    print("\n" + "=" * 60)
    print("Setup complete.  Next:")
    print()
    print("  # Run the steady-state simulation:")
    print("  cd /data/rim2d/nbo_2026/v4")
    print("  export LD_LIBRARY_PATH=/data/rim2d/lib:$LD_LIBRARY_PATH")
    print("  ../../bin/RIM2D simulation_v4ss.def --def flex")
    print()
    print("  # Extract IWD from final output:")
    print("  cd /data/rim2d/nbo_2026")
    print("  micromamba run -n zarrv3 python setup_v4.py --extract-iwd")
    print("=" * 60)


if __name__ == "__main__":
    main()
