# Step 4 — Burn DEM
# ---------------------------------------------------------------------------

def step4_burn_dem(gdf, dem, x, y, ref_transform, ref_crs):
    print()
    print("=" * 60)
    print("Step 4 — Burn river channels into DEM")
    print("=" * 60)

    nrows, ncols = dem.shape
    burned_dem   = dem.copy()
    depth_raster = np.zeros_like(dem)

    # Build rasterio transform in y-ascending convention for rasterize
    dx = x[1] - x[0]
    dy = y[1] - y[0]
    # rasterio uses top-left corner, y-descending
    ras_transform = rasterio.transform.from_origin(
        x[0] - dx / 2,
        y[-1] + dy / 2,   # top of domain
        dx, dy
    )

    burned_count = 0
    for _, row in gdf.iterrows():
        geom  = row.geometry
        w     = float(row["rivwth"])
        d     = float(row["rivdph"])

        if geom.is_empty:
            continue

        # Buffer the centerline by half-width
        buf = geom.buffer(w / 2.0)

        # Rasterize buffer → mask (y-descending for rasterio)
        mask = rio_rasterize(
            [(mapping(buf), 1)],
            out_shape=(nrows, ncols),
            transform=ras_transform,
            fill=0,
            dtype=np.uint8,
        )
        # Flip to y-ascending (match our arrays)
        mask = mask[::-1, :]

        cells = mask > 0
        if not cells.any():
            continue

        # Lower DEM by depth, track max depth per cell
        burned_dem[cells]   = np.minimum(burned_dem[cells], dem[cells] - d)
        depth_raster[cells] = np.maximum(depth_raster[cells], d)
        burned_count += int(cells.sum())

    # IWD = original - burned, clipped to ≥ 0
    iwd = np.maximum(dem - burned_dem, 0.0)
    iwd[~np.isfinite(iwd)] = 0.0

    wet = iwd > 0
    print(f"  Rivers burned      : {len(gdf)}")
    print(f"  Total cells burned : {int(wet.sum()):,}  "
          f"({100 * wet.sum() / dem.size:.2f}% of domain)")
    print(f"  IWD range          : {iwd[wet].min():.2f} – {iwd[wet].max():.2f} m")
    print(f"  Burned DEM min     : {np.nanmin(burned_dem):.1f} m")

    channel_mask = wet.astype(np.float64)
    return burned_dem, iwd, channel_mask


