"""
Nairobi v5 — TDX-Hydro river network burn IWD.

Uses the pre-downloaded TDX-Hydro v2 river network
(river_network_tdx_v2.geojson, 141 segments, orders 2–5) instead of
HydroRIVERS, and assigns width + depth by stream order using standard
tropical-river hydraulic scaling.

  Step 1 — Load TDX-Hydro river network (orders 2–5)
  Step 2 — Assign width + depth by stream order
  Step 3 — Burn DEM: buffer each segment by rivwth/2, lower by rivdph
  Step 4 — IWD = original DEM - burned DEM (clipped to ≥ 0)
  Step 5 — Write v5/input/ + visualization

Stream-order width/depth table (Nairobi tropical rivers):
  Order 2 — W= 15m, D=0.5m   (headwater streams)
  Order 3 — W= 30m, D=1.0m
  Order 4 — W= 60m, D=1.5m
  Order 5 — W=120m, D=2.5m   (Nairobi / Athi R.)

Inputs (all pre-existing):
  v1/tif/dem.tif                    — 30m Copernicus DEM
  v1/input/river_network_tdx_v2.geojson — TDX-Hydro river network

Outputs → v5/input/
  dem.nc, iwd.nc, channel_mask.nc
  roughness.nc (from v3b Dynamic World), + buildings, sealed, pervious, sewershed

Usage:
    cd /data/rim2d/nbo_2026
    micromamba run -n hydromt-wflow python setup_v5.py
"""

