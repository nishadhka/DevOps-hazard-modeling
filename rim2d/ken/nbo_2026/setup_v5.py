def step5_save(burned_dem, iwd, channel_mask, x, y):
    print()
    print("=" * 60)
    print("Step 5 — Save v5 inputs")
    print("=" * 60)
    INPUT_DIR.mkdir(parents=True, exist_ok=True)

    write_rim2d_nc(burned_dem,    x, y, INPUT_DIR / "dem.nc")
    print("  Written: dem.nc  (stream-burned)")
    write_rim2d_nc(iwd,           x, y, INPUT_DIR / "iwd.nc")
    print("  Written: iwd.nc")
    write_rim2d_nc(channel_mask,  x, y, INPUT_DIR / "channel_mask.nc")
    print("  Written: channel_mask.nc")

    # Use v3b roughness (Dynamic World) if available, else v1
    roughness_src = V3B_INPUT / "roughness.nc"
    if not roughness_src.exists():
        roughness_src = V1_INPUT / "roughness.nc"
        print("  NOTE: v3b roughness not found, using v1 WorldCover roughness")

    copy_list = {
        "roughness.nc":        roughness_src,
        "buildings.nc":        V1_INPUT / "buildings.nc",
        "sealed_surface.nc":   V1_INPUT / "sealed_surface.nc",
        "pervious_surface.nc": V1_INPUT / "pervious_surface.nc",
        "sewershed_v1_full.nc":V1_INPUT / "sewershed_v1_full.nc",
    }
    for fname, src in copy_list.items():
        dst = INPUT_DIR / fname
        if src.exists():
            shutil.copy2(str(src), str(dst))
            print(f"  Copied:  {fname}")
        else:
            print(f"  WARNING: {fname} not found at {src}")


