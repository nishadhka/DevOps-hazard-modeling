def step6_visualize(gdf, dem, burned_dem, iwd, x, y):
    print()
    print("=" * 60)
    print("Step 6 — Visualization")
    print("=" * 60)
    VIS_DIR.mkdir(parents=True, exist_ok=True)

    from matplotlib.colors import LightSource
    ls = LightSource(azdeg=315, altdeg=45)
    hs = ls.hillshade(np.nan_to_num(dem, nan=0), vert_exag=3, dx=30, dy=30)

    x_km = (x - x[0]) / 1000.0
    y_km = (y - y[0]) / 1000.0
    ext  = [float(x_km[0]), float(x_km[-1]),
            float(y_km[0]), float(y_km[-1])]

    fig, axes = plt.subplots(1, 3, figsize=(22, 8))

    # --- Panel 1: TDX river network coloured by stream order ---
    ax = axes[0]
    ax.imshow(hs, origin="lower", extent=ext, cmap="gray",
              vmin=0.3, vmax=1.0, alpha=0.7)
    order_colors = {2: "#74b9ff", 3: "#0984e3", 4: "#6c5ce7", 5: "#d63031"}
    order_lw     = {2: 0.6, 3: 1.2, 4: 2.0, 5: 3.0}
    import matplotlib.patches as mpatches
    handles = []
    for order in sorted(order_colors):
        sub = gdf[gdf["stream_order"] == order]
        for _, row in sub.iterrows():
            geom = row.geometry
            if geom.is_empty:
                continue
            segs = [geom] if geom.geom_type == "LineString" else list(geom.geoms)
            for seg in segs:
                xs_g = [(xv - x[0]) / 1000 for xv in seg.xy[0]]
                ys_g = [(yv - y[0]) / 1000 for yv in seg.xy[1]]
                ax.plot(xs_g, ys_g, color=order_colors[order],
                        lw=order_lw[order], alpha=0.9)
        w = ORDER_WIDTH[order]; d = ORDER_DEPTH[order]
        handles.append(mpatches.Patch(color=order_colors[order],
            label=f"Order {order} — W={w}m, D={d}m ({len(sub)} segs)"))
    ax.legend(handles=handles, loc="lower right", fontsize=8, framealpha=0.9)
    ax.set_title(f"TDX-Hydro v2 River Network ({len(gdf)} segments)\n"
                 "coloured by stream order",
                 fontsize=11, fontweight="bold")
    ax.set_xlabel("x (km)"); ax.set_ylabel("y (km)")

    # --- Panel 2: Burn depth (DEM diff) ---
    ax = axes[1]
    diff = np.maximum(dem - burned_dem, 0)
    diff_disp = np.ma.masked_where(diff <= 0, diff)
    ax.imshow(hs, origin="lower", extent=ext, cmap="gray",
              vmin=0.3, vmax=1.0, alpha=0.55)
    im2 = ax.imshow(diff_disp, origin="lower", extent=ext,
                    cmap="YlOrRd", vmin=0, vmax=diff[diff > 0].max() if diff.any() else 5,
                    alpha=0.85, interpolation="nearest")
    fig.colorbar(im2, ax=ax, shrink=0.7, label="Burn depth (m)")
    n_burned = int((diff > 0).sum())
    ax.set_title(f"Stream-Burned DEM\n({n_burned:,} cells burned, "
                 f"max burn {diff.max():.1f} m)",
                 fontsize=11, fontweight="bold")
    ax.set_xlabel("x (km)"); ax.set_ylabel("y (km)")

    # --- Panel 3: IWD ---
    ax = axes[2]
    iwd_disp = np.ma.masked_where(iwd <= 0, iwd)
    ax.imshow(hs, origin="lower", extent=ext, cmap="gray",
              vmin=0.3, vmax=1.0, alpha=0.55)
    im3 = ax.imshow(iwd_disp, origin="lower", extent=ext,
                    cmap="Blues", vmin=0, vmax=iwd[iwd > 0].max() if (iwd > 0).any() else 5,
                    alpha=0.85, interpolation="nearest")
    fig.colorbar(im3, ax=ax, shrink=0.7, label="IWD (m)")
    wet = int((iwd > 0).sum())
    ax.set_title(f"Initial Water Depth (v5)\n({wet:,} wet cells, "
                 f"mean {iwd[iwd > 0].mean():.2f} m)",
                 fontsize=11, fontweight="bold")
    ax.set_xlabel("x (km)"); ax.set_ylabel("y (km)")

    for ax in axes:
        ax.xaxis.set_major_formatter(plt.FuncFormatter(lambda v, _: f"{v:.0f}"))
        ax.yaxis.set_major_formatter(plt.FuncFormatter(lambda v, _: f"{v:.0f}"))

    fig.suptitle(
        "Nairobi v5 — TDX-Hydro River Burn IWD\n"
        f"Width/depth by stream order  (orders 2–5)",
        fontsize=13, fontweight="bold"
    )
    fig.tight_layout()
    out = VIS_DIR / "v5_hydrorivers_burn_iwd.png"
    fig.savefig(str(out), dpi=DPI, bbox_inches="tight", facecolor="white")
    plt.close(fig)
    print(f"  Saved: {out}")


