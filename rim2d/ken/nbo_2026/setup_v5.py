def step1_load_rivers(target_crs):
    print("=" * 60)
    print("Step 1 — Load TDX-Hydro v2 river network")
    print("=" * 60)

    gdf = gpd.read_file(str(TDX_RIVER_PATH))
    gdf = gdf.to_crs(target_crs)

    print(f"  Segments loaded : {len(gdf)}")
    for order in sorted(gdf["stream_order"].unique()):
        n = (gdf["stream_order"] == order).sum()
        w = ORDER_WIDTH.get(int(order), 15)
        d = ORDER_DEPTH.get(int(order), 0.5)
        print(f"  Order {order} : {n:3d} segments  →  W={w}m, D={d}m")

    return gdf


